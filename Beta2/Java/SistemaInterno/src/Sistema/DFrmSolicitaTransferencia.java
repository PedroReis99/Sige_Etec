    public void Solicitar(){
    
        ControleSolicitacao cs = new ControleSolicitacao();
        cs.RegistrarSolicitacao(txtRA.getText(), Periodo, (String) ComboSerie.getSelectedItem(),
                (String) ComboTurma.getSelectedItem(), CodEscola, Status);
        
    }
    
    public void PesquisaInfos(){
    
        AlunoBean ab = new AlunoBean();
        ab.setId_aluno(txtRA.getText());
        
        ControleAluno ca = new ControleAluno();
        ca.PesquisaSolicitacao(ab);
        
        txtAluno.setText(ab.getNomeAluno());
        txtResponsavel.setText(ab.getInner1());
        txtEmail.setText(ab.getInner2());
    }
    
    public void Pesquisa(){
       
        Variavel = " where Aluno like '%"+txtRA.getText()+"%' and Stats = 'Ativo'";
        
        ControleSolicitacao cs = new ControleSolicitacao();
        DefaultTableModel model = (DefaultTableModel) tblSolicitacoes.getModel();
        model.setNumRows(0);
        for(SolicitacaoBean sb : cs.Listar(Variavel)){
            model.addRow(new Object[]{
                
                sb.getId(),
                sb.getAluno(),
                sb.getInner1(),
                sb.getInner2()
            });
        }
    }
