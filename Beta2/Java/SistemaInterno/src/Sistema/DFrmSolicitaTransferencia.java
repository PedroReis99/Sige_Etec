    private void lblSolicitarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblSolicitarMouseClicked
        if(txtRA.getText().isEmpty()|| Periodo.equals("") || txtResponsavel.getText().isEmpty()||
                txtEmail.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Por favor pesquise as informações solicitadas pelo RA do aluno que será transferido!"
            ,"Aviso",JOptionPane.INFORMATION_MESSAGE);
        }else{
            Solicitar();
            JOptionPane.showMessageDialog(null, "Solicitação realizada com sucesso!","Aviso",JOptionPane.INFORMATION_MESSAGE);
            
            btnPesquisa2ActionPerformed(null);
        }
    }//GEN-LAST:event_lblSolicitarMouseClicked

